int	max(int *tab, unsigned int len)
{
	int b;
	int i;

	i = 0;
	if (!tab)
		return (0);
	b = tab[i];
	while (i < len)
	{
		if (tab[i] > b)
			b = tab[i];
		i++;
	}
	return (b);
}
